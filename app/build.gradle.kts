val localPropertiesFile = rootProject.file("local.properties")
if (localPropertiesFile.exists()) {
    localPropertiesFile.inputStream().use { localProperties.load(it) }
}

// Get the API key, or throw a clear error if it's not found.
val geminiApiKey = localProperties.getProperty("GEMINI_API_KEY")
if (geminiApiKey == null) {
    throw GradleException("GEMINI_API_KEY not found in local.properties file. " +
            "Please create a local.properties file in the root of your project and add the following line:\n" +
            "GEMINI_API_KEY=\"YOUR_API_KEY_HERE\"")
}


